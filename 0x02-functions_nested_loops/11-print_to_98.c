#include "main.h"
/**
 * print_to_98 - print it
 * @n: first variable
 */
	void print_to_98(int n)
{
	if (n >= 98)
{
	do {
	_putchar('n');
	_putchar(',');
	_putchar(' ');
	n--;
	} while (n <= 98);
	_putchar('\n');
}
	else
{
	while (n <=98)
	{
	_putchar('n');
	_putchar(',');
	_putchar(' ');
	n++;
	}
}
}
